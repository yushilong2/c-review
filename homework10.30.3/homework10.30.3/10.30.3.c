//逆序的三位数
//程序每次读入一个正三位数，然后输出按位逆序的数字。注意：当输入的数字含有结尾的0时，输出不应带有前导的0.比如输入700，输出应该是7
//输入格式：每个测试是一个3位的正整数
//输出和格式：输出按位逆序的数
#include<stdio.h>
int main() 
{
	int m;
	scanf("%d", &m);
	int a, b, c, n;
	a = m / 100;
	b = ((m % 100) / 10);
	c = ((m % 100) % 10);
	n = c * 100 + b * 10 + a;
	printf("%d", n);
	return 0;
}